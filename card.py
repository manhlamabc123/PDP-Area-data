class Card:
    def __init__(self, image, name, type, nation, race, grade, power, critical, shield, skill, effect, regulation, number, rarity):
        self.image = image
        self.name = name
        self.type = type
        self.nation = nation
        self.race = race
        self.grade = grade
        self.power = power
        self.critical = critical
        self.shield = shield
        self.skill = skill
        self.effect = effect
        self.regulation = regulation
        self.number = number
        self.rarity = rarity